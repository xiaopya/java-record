package com.itheima.note;

/**
 * 关系运算符
 * 结果为： boolean类型
 */
public class Note12 {
    public static void main(String[] args) {
        int a = 10;
        int b = 11;
        System.out.println(a>b);
        System.out.println(a<b);
        System.out.println(a>=b);
        System.out.println(a<=b);
        System.out.println(a==b);
        System.out.println(a!=b);
    }
}
