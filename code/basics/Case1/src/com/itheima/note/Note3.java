package com.itheima.note;

/**
 * 变量
 * 数据类型 变量名 = 数据;
 */
public class Note3 {
    public static void main(String[] args) {

        // 整型
        int age = 18;
        System.out.println(age);

        // 小数类型
        double score = 11.5;
        System.out.println(score);

        // 变量的特点
        int age2 = 18;
        System.out.println(age2);
        age2  = age2 + 1;
        System.out.println(age2);
        age2 = 199;
        System.out.println(age2);

        double money = 9.5;
        money = money + 10 - 5;
        System.out.println(money);


    }
}
